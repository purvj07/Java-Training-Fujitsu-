package day1;

public class ColorCode {

	public static void main(String[] args) {
		char ch= 'R';
		switch(ch) {
		case 'R': System.out.println("R->Red");
		break;
		case 'B': System.out.println("R->Blue");
		break;
		case 'G': System.out.println("R->Green");
		break;
		case 'O': System.out.println("R->Orange");
		break;
		case 'Y': System.out.println("R->Yellow");
		break;
		case 'W': System.out.println("R->White");
		break;
		default: System.out.println("Invalid");
		}

	}

}
